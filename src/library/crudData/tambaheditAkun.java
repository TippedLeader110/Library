    public tambaheditAkun(String val){
        initComponents();
        this.val = val;
        
         try{
            ResultSet rs_id = stmt.executeQuery("Select distinct id_petugas from perpus.petugas");
            while(rs_id.next()){
                String id = rs_id.getString("id_petugas");
                idCB.addItem(id); 
            }   
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Kesalahan : " + ex, "Kesalahan", JOptionPane.ERROR_MESSAGE);   
        }
         
        title.setText("Edit Akun");
        try{
            ResultSet rs = stmt.executeQuery("SELECT id_petugas, username, password FROM perpus.petugas where id_petugas = " + val);
            while (rs.next()){
               idCB.setSelectedItem(rs.getString("id_petugas"));
               userField.setText(rs.getString("username"));
               passField.setText(rs.getString("password"));
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Kesalahan : " + ex, "Kesalahan", JOptionPane.ERROR_MESSAGE);   
        }
    }

